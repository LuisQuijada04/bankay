package com.example.alumno.myapplication.models;

public class Client {
    private String username;
    private String firts_name;
    private String Last_name;
    private int year_olb;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirts_name() {
        return firts_name;
    }

    public void setFirts_name(String firts_name) {
        this.firts_name = firts_name;
    }

    public String getLast_name() {
        return Last_name;
    }

    public void setLast_name(String last_name) {
        Last_name = last_name;
    }

    public int getYear_olb() {
        return year_olb;
    }

    public void setYear_olb(int year_olb) {
        this.year_olb = year_olb;
    }
}
